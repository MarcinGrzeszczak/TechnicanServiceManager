package mg.backend;

abstract class Entity implements DatabaseContract {
    protected String name;
    protected long id;
    
    Entity(long id, String name) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return this.id;
    }

    public void setId(long id) {
        this.id = id;
    }

    abstract String show();


}